// Helper function to get books organized by class
export const getBooksByClass = (books: NCERTBook[] = ncertBooks): Record<string, NCERTBook[]> => {
  return books.reduce((acc, book) => {
    if (!acc[book.className]) {
      acc[book.className] = [];
    }
    acc[book.className].push(book);
    return acc;
  }, {} as Record<string, NCERTBook[]>);
};
